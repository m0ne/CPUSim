function hexStringToUint8Array(hexString: string): Uint8Array {
  const cleanedHexString = hexString.split(' ').join('').split('\n').join('');
  /* eslint-disable */
  // @ts-ignore
  return Uint8Array.from(Buffer.from(cleanedHexString, 'hex'));
  /* eslint-enable */
}

